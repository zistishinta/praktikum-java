package pertemuan_5;

public class praktik_elseIf {

    public static void main(String[] args) {
        int nilai = 80;
        
        if (nilai > 90) {
            System.out.println("Nilai Anda Bagusu !!!!");
        } 
        else if (nilai > 60) {
            System.out.println("Nilai Anda Kurang.");
        }
        else {
            System.out.println("Maaf, anda tidak lulus..");
        }
    }
    
}
